#include "ft_printf.h"

int	set_flag(t_data *data, char c)
{
	int i;

	i = 0;
	while (i < 5)
	{
		if ((data->f)[i] == '\0')
		{
			(data->f)[i] = c;
			break ;
		}
		else if ((data->f)[i] == c)
			break ;
		i++;
	}
	return (1);
}
