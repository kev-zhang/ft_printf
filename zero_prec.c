#include "ft_printf.h"

int		zero_prec(t_data *data)
{
	int	i;

	if ((data->fw)[0] == 1 && (data->fw)[1] > 0)
	{
		i = 0;
		while (i < (data->fw)[1])
		{
			ft_putchar(' ');
			i++;
		}
		return ((data->fw)[1]);
	}
	return (0);
}
