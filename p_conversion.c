#include "ft_printf.h"

int			p_conversion(t_data *data, va_list ap)
{
	set_flag(data, '#');
	(data->p)[0] = 0;
	(data->p)[1] = 0;
	(data->lm)[0] = 'l';
	(data->lm)[1] = '\0';
	return (ux_conversion(data, ap, 16));
}
