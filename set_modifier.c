#include "ft_printf.h"

int	set_modifier(t_data *d, char *f)
{
	if ((d->lm)[0] == 'l' || (d->lm)[0] == 'j' || (d->lm)[0] == 'z')
	{
		if (f[0] == 'l' && f[1] == 'l')
			return (2);
		return (1);
	}
	else if ((d->lm)[0] == 'h' && (d->lm)[1] == '\0' && f[0] == 'h')
	{
		if (f[0] == 'h' && f[1] == 'h')
			return (2);
		return (1);
	}
	else
	{
		(d->lm)[0] = f[0];
		(d->lm)[1] = '\0';
		if ((f[0] == 'h' && f[1] == 'h') || (f[0] == 'l' && f[1] == 'l'))
		{
			(d->lm)[1] = f[1];
			return (2);
		}
		return (1);
	}
}
